package j.jave.framework.components.core.menu;

/**
 * item.
 * @author J
 */
public class Item {

	/**
	 * url
	 */
	private String url;
	
	/**
	 * param
	 */
	private String param;
	
	
	/**
	 * label
	 */
	private String label;
	
	
	/**
	 * name
	 */
	private String name;
	
	/**
	 * parent .
	 */
	private Menu menu;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getParam() {
		return param;
	}

	public void setParam(String param) {
		this.param = param;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Menu getMenu() {
		return menu;
	}

	public void setMenu(Menu menu) {
		this.menu = menu;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
