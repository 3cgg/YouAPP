package j.jave.kernal.zookeeper;

public class JValue {

	private JStatWrapper statWrapper;

	private byte[] bytes;

	public JStatWrapper getStatWrapper() {
		return statWrapper;
	}

	public void setStatWrapper(JStatWrapper statWrapper) {
		this.statWrapper = statWrapper;
	}

	public byte[] getBytes() {
		return bytes;
	}

	public void setBytes(byte[] bytes) {
		this.bytes = bytes;
	}

}
