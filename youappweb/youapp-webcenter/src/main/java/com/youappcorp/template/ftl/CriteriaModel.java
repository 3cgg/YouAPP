package com.youappcorp.template.ftl;

public class CriteriaModel {

	private String criteriaPackage;
	
	private String criteriaClassName;
	
	private String criteriaSimpleClassName;

	private String variableName;

	public String getCriteriaPackage() {
		return criteriaPackage;
	}

	public void setCriteriaPackage(String criteriaPackage) {
		this.criteriaPackage = criteriaPackage;
	}

	public String getCriteriaClassName() {
		return criteriaClassName;
	}

	public void setCriteriaClassName(String criteriaClassName) {
		this.criteriaClassName = criteriaClassName;
	}

	public String getCriteriaSimpleClassName() {
		return criteriaSimpleClassName;
	}

	public void setCriteriaSimpleClassName(String criteriaSimpleClassName) {
		this.criteriaSimpleClassName = criteriaSimpleClassName;
	}

	public String getVariableName() {
		return variableName;
	}

	public void setVariableName(String variableName) {
		this.variableName = variableName;
	}
	
	
	
}
